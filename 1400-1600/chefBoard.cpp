void solve()
{
    int n;
    cin >> n;
    int tot = n*n;
    for (int i = 3; i < n+1; i+=2) {
        tot+=(n-i+1)*(n-i+1);
    }
    cout << tot << endl;
}

int main()
{
	ios::sync_with_stdio(0);
	cin.tie(0), cout.tie(0);
   	ll t;
    	cin >> t;
    	while (t--)
    	{
        	solve();
    	}
    	return 0;
}
