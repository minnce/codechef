void solve()
{
    int n,q;
    cin >> n >> q;
    int curr = 0;
    ll tot = 0;
    for (int i = 0; i < q; i++) {
        int d1,d2;
        cin >> d1 >> d2;
        tot+=abs(curr-d1);
        tot+=abs(d1-d2);
        curr = d2;
    }
    cout << tot << endl;
}

